function capitalize(word: string) {
    return word.charAt(0).toUpperCase() + word.slice(1)
}

export function randomTitle() {
    const adjective = capitalize(adjectives[Math.floor(Math.random() * adjectives.length)])
    const color = capitalize(colors[Math.floor(Math.random() * colors.length)])
    
    return `${adjective}${color}`
}